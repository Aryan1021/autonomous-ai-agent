class RegenerationRequest(AgentBaseModel):
    """
    Input provided to the Regenerator.
    """

    execution: ExecutionResult = Field(
        ...,
        description="Original execution result.",
    )

    reflection: ReflectionResult = Field(
        ...,
        description="Reflection feedback.",
    )

class RegenerationResult(AgentBaseModel):
    """
    Result returned by the Regenerator.
    """

    execution: ExecutionResult = Field(
        ...,
        description="Improved execution result.",
    )

    iteration: int = Field(
        default=1,
        ge=1,
        description="Regeneration iteration number.",
    )

    improved: bool = Field(
        default=True,
        description="Whether improvements were applied.",
    )

class DocumentSection(AgentBaseModel):
    """
    Represents a single section in the generated document.
    """

    heading: str = Field(
        ...,
        min_length=1,
        description="Section heading.",
    )

    content: str = Field(
        ...,
        description="Section content.",
    )

class DocumentContext(AgentBaseModel):
    """
    Structured context used by the document generator.
    """

    title: str = Field(
        ...,
        description="Document title.",
    )

    goal: str = Field(
        ...,
        description="Primary goal.",
    )

    assumptions: list[str] = Field(
        default_factory=list,
        description="Planner assumptions.",
    )

    sections: list[DocumentSection] = Field(
        default_factory=list,
        description="Document sections.",
    )

    reflection: ReflectionContext = Field(
        ...,
        description="Reflection report.",
    )

    summary: str = Field(
        default="",
        description="Workflow summary.",
    )


class DocumentResult(AgentBaseModel):
    """
    Metadata returned after generating the document.
    """

    title: str = Field(
        ...,
        description="Document title.",
    )

    filename: str = Field(
        ...,
        description="Generated filename.",
    )

    document_path: str = Field(
        ...,
        description="Location of the generated document.",
    )

    generated_at: datetime = Field(
        default_factory=datetime.now,
        description="Generation timestamp.",
    )

    section_count: int = Field(
        ...,
        ge=0,
        description="Number of generated sections.",
    )

    word_count: int = Field(
        ...,
        ge=0,
        description="Approximate document word count.",
    )