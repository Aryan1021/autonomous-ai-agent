"""
Data models used by the Autonomous AI Agent.
"""

from __future__ import annotations

from typing import Literal

from pydantic import BaseModel, ConfigDict, Field


class AgentBaseModel(BaseModel):
    """
    Base model for all AI agent models.

    Provides common validation rules and configuration.
    """

    model_config = ConfigDict(
        extra="forbid",
        str_strip_whitespace=True,
    )


class Task(AgentBaseModel):
    """
    Represents a single executable task generated by the planner.
    """

    task_id: int = Field(
        ...,
        ge=1,
        description="Sequential task identifier.",
    )

    title: str = Field(
        ...,
        min_length=3,
        max_length=100,
        description="Short title describing the task.",
    )

    status: Literal[
        "pending",
        "completed",
        "failed",
    ] = Field(
        default="pending",
        description="Current execution status.",
    )

    output: str | None = Field(
        default=None,
        description="Generated output after task execution.",
    )

    error: str | None = Field(
        default=None,
        description="Error message if task execution fails.",
    )


class PlannerOutput(AgentBaseModel):
    """
    Structured execution plan generated by the planner.
    """

    goal: str = Field(
        ...,
        min_length=1,
        description="Primary objective derived from the user's request.",
    )

    assumptions: list[str] = Field(
        default_factory=list,
        description=(
            "Reasonable assumptions made by the planner "
            "when information is incomplete."
        ),
    )

    tasks: list[Task] = Field(
        default_factory=list,
        min_length=1,
        description="Ordered list of executable tasks.",
    )